    function formatStatusTag(status) {
      if (status === "Approved") {
        return '<span class="tag tag-approved">Approved</span>';
      }
      if (status === "Under Review") {
        return '<span class="tag tag-review">Under Review</span>';
      }
      if (status === "Not Started") {
        return '<span class="tag tag-started">Not Started</span>';
      }
      return '<span class="tag tag-pending">Pending</span>';
    }

    function statusRowClass(status) {
      if (status === "Approved") {
        return "status-row-approved";
      }
      if (status === "Under Review") {
        return "status-row-review";
      }
      if (status === "Not Started") {
        return "status-row-started";
      }
      return "status-row-pending";
    }

    function stationMetrics(station) {
      const data = stationData[station];
      const sample = data.sampleSubmissionTracker;
      const totalItems = Object.values(data).reduce((sum, items) => sum + items.length, 0);
      const completedItems = Object.values(data).reduce(
        (sum, items) => sum + items.filter((item) => item.completionDate).length,
        0
      );
      const progress = totalItems ? Math.round((completedItems / totalItems) * 100) : 0;
      const approved = sample.filter((item) => item.approvalStatus === "Approved").length;
      const review = sample.filter((item) => item.approvalStatus === "Under Review").length;
      const pending = sample.filter((item) => item.approvalStatus === "Pending" || item.approvalStatus === "Not Started").length;
      let overallStatus = "Needs Attention";
      let overallClass = "status-pending";

      if (progress >= 55) {
        overallStatus = "On Track";
        overallClass = "status-approved";
      } else if (progress >= 25) {
        overallStatus = "In Progress";
        overallClass = "status-review";
      }

      return { totalItems, completedItems, progress, approved, review, pending, overallStatus, overallClass };
    }

    function titleFromKey(key) {
      return key
        .replace(/([A-Z])/g, " $1")
        .replace(/^./, (char) => char.toUpperCase());
    }

    function buildTable(section, rows) {
      const rowItems = Array.isArray(rows) ? rows : [];
      const bodyRows = rowItems.map((row) => {
        const values = Object.values(row);
        const rowStatus = row.approvalStatus || row.sampleSubmission || row.formSubmission || "";
        const rowClass = rowStatus ? statusRowClass(rowStatus) : "";

        const cells = values.map((value, index) => {
          if (index === 3 && row.approvalStatus) {
            return `<td>${formatStatusTag(value)}</td>`;
          }
          return `<td>${value || "-"}</td>`;
        }).join("");

        return `<tr class="${rowClass}">${cells}</tr>`;
      }).join("");

      return `
        <article class="table-card">
          <h3>${section.title || titleFromKey(section.key)}</h3>
          <p class="small-copy">${rowItems.length} tracked item${rowItems.length === 1 ? "" : "s"}</p>
          <div class="table-wrap">
            <table>
              <thead>
                <tr>
                  ${(section.columns || []).map((column) => `<th>${column}</th>`).join("")}
                </tr>
              </thead>
              <tbody>
                ${bodyRows || `<tr><td colspan="${(section.columns || []).length || 1}">No items yet.</td></tr>`}
              </tbody>
            </table>
          </div>
        </article>
      `;
    }


